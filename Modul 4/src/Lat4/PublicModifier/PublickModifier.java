public class PublickModifier {
    public int a = 2;
    public int b = 5;
    public int c = 9;
    public void kali(){
        int d = a*b*c;
        System.out.println("Hasil kali = " + d);
    }
    
public void tambah(){
    int e = a+b+c;
    System.out.println("Hasil tambah = " + e);
}
public void kurang(){
    int e = a-b-c;
    System.out.println("Hasil kurang = " + e);
}
public void bagi(){
    int e = a/b/c;
    System.out.println("Hasil bagi = " + e);
}
public void rata_rata(){
    int e = (a+b+c)/3;
    System.out.println("Hasil rata-rata = " + e);
}
}
